export const countryList = Object.values(countries);

export const countriesByNameEn = countryList.reduce(
  (result, country) => ({
    ...result,
    [country.nameEn]: country,
  }),
  {} as Record<string, CountryResource>,
);

countriesByNameEn["Curaçao"] = countries.curacao;
countriesByNameEn.German = countries.germany;

export function getCountryByNameEn(team: string) {
  return countriesByNameEn[team];
}

export function getCountryDisplayName(team: string) {
  return getCountryByNameEn(team)?.nameZh ?? team;
}

